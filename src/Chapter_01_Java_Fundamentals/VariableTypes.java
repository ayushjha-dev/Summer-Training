package Chapter_01_Java_Fundamentals;

public class VariableTypes {

    public static void main(String[] args) {
        // Integer type
        int wholeNumber = 100;
        System.out.println("An integer variable: " + wholeNumber);

        // Floating-point type (single precision)
        float decimalNumber = 3.14f; // 'f' suffix is important for float literals
        System.out.println("A float variable: " + decimalNumber);

        // String type (sequence of characters)
        String greeting = "Hello, Java!";
        System.out.println("A String variable: " + greeting);
    }
}
